                function filterInt(item) {
                    var value = parseInt(item.val(), 10);
                    if (isNaN(value) || value < 0) {
                        value = '';
                    } else if (value == 0) {
                        value = 1;
                    }
                    $(item).val(value);

                    checkPageNumberMax(item);

                    if (item.val() != parseInt(item.val()) && item.val() < 1) {
                        return false;
                    }
